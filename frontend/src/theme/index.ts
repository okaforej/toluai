// Risk score utility
export const getRiskColor = (score: number) => {
  if (score <= 20) return colors.risk.low;
  if (score <= 40) return colors.risk.moderateLow;
  if (score <= 60) return colors.risk.moderate;
  if (score <= 80) return colors.risk.moderateHigh;
  return colors.risk.high;
};

export const getRiskLevel = (score: number) => {
  if (score <= 20) return 'Low Risk';
  if (score <= 40) return 'Moderate-Low Risk';
  if (score <= 60) return 'Moderate Risk';
  if (score <= 80) return 'Moderate-High Risk';
  return 'High Risk';
};

